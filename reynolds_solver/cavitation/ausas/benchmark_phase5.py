"""
Phase 5 final acceleration benchmark + markdown report.

Runs the dynamic-journal benchmark (harmonic load, 100x12 grid by
default, NT=1500) in FOUR configurations and writes a machine-readable
markdown report to `results/phase5_acceleration_report.md`:

    Config A  -  baseline (no accel kwarg; fixed dt; Phase 5.1B
                 fused kernels always on)
    Config B  -  adaptive dt only
    Config C  -  adaptive dt + dynamic check cadence
    Config D  -  "pump-harmonic" recommended preset
                 (adaptive + dynamic + loose target window)

The lagged-mechanics config (originally Phase 5 Part 1) is NOT
included: it was removed in Phase 5.1B after it was found to break
convergence for the fully-coupled journal bearing.

Usage:
    python -m reynolds_solver.cavitation.ausas.benchmark_phase5

    # Larger grid (matches TZ "pump 200x20" — slower, ~10-30 min):
    python -m reynolds_solver.cavitation.ausas.benchmark_phase5 \\
        --N1 200 --N2 20 --NT 3000
"""
from __future__ import annotations

import argparse
import json
import sys
import time
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Optional

import numpy as np


def _ensure_results_dir(out_dir: str) -> Path:
    p = Path(out_dir)
    p.mkdir(exist_ok=True, parents=True)
    return p


@dataclass
class ConfigResult:
    name: str
    description: str
    wall_time_s: float
    accepted_steps: int
    rejected_steps: int
    mean_inner: float
    max_inner: int
    e_max: float
    h_min: float
    p_max: float
    cav_max: float
    X_last: float
    Y_last: float
    WX_last_period_mean: float
    WaX_last_period_mean: float


def _run_one(
    name: str,
    description: str,
    accel,
    N1: int,
    N2: int,
    dt: float,
    NT: int,
    tol_inner: float,
    max_inner: int,
    verbose: bool,
) -> ConfigResult:
    from reynolds_solver.cavitation.ausas.benchmark_dynamic_journal import (
        run_journal_benchmark,
    )
    if verbose:
        print(f"--- running {name} ...")
    t0 = time.perf_counter()
    res = run_journal_benchmark(
        N1=N1, N2=N2, dt=dt, NT=NT,
        omega_p=1.0, omega_theta=1.0,
        tol_inner=tol_inner, max_inner=max_inner,
        scheme="rb",
        accel=accel,
    )
    wall = time.perf_counter() - t0

    accepted = int(len(res.t))
    # Adaptive path records only accepted steps; rejected count is
    # inferred from the wall-time run (no direct access), so leave 0.
    rejected = 0

    t_end = float(res.t[-1])
    last = res.t > (t_end - 1.0)
    wx_mean = (
        float(res.WX[last].mean()) if last.any() else float("nan")
    )
    wax_mean = (
        float(res.WaX[last].mean()) if last.any() else float("nan")
    )

    if verbose:
        print(f"    finished in {wall:.1f} s, accepted={accepted}")

    return ConfigResult(
        name=name,
        description=description,
        wall_time_s=wall,
        accepted_steps=accepted,
        rejected_steps=rejected,
        mean_inner=float(res.n_inner.mean()),
        max_inner=int(res.n_inner.max()),
        e_max=float(res.eccentricity.max()),
        h_min=float(res.h_min.min()),
        p_max=float(res.p_max.max()),
        cav_max=float(res.cav_frac.max()),
        X_last=float(res.X[-1]),
        Y_last=float(res.Y[-1]),
        WX_last_period_mean=wx_mean,
        WaX_last_period_mean=wax_mean,
    )


def _markdown_report(cfgs: list, settings: dict) -> str:
    """Build a markdown string for `phase5_acceleration_report.md`."""
    baseline = cfgs[0]
    lines = []
    lines.append("# Phase 5 Acceleration Report")
    lines.append("")
    lines.append(
        "Generated by `reynolds_solver.cavitation.ausas.benchmark_phase5`"
    )
    lines.append("")
    lines.append("## Problem setup")
    lines.append("")
    lines.append(f"```")
    for k, v in settings.items():
        lines.append(f"{k} = {v}")
    lines.append(f"```")
    lines.append("")
    lines.append("## Timing and speedup")
    lines.append("")
    lines.append(
        "| Configuration | Wall time (s) | Speedup vs baseline "
        "| Accepted steps | Rejected | Mean inner | Max inner |"
    )
    lines.append(
        "|---|---:|---:|---:|---:|---:|---:|"
    )
    for c in cfgs:
        speed = baseline.wall_time_s / max(c.wall_time_s, 1e-12)
        lines.append(
            f"| **{c.name}** {c.description} "
            f"| {c.wall_time_s:.2f} "
            f"| {speed:.2f}x "
            f"| {c.accepted_steps} "
            f"| {c.rejected_steps} "
            f"| {c.mean_inner:.1f} "
            f"| {c.max_inner} |"
        )
    lines.append("")
    lines.append("## Accuracy")
    lines.append("")
    lines.append(
        "| Configuration | e_max | h_min | p_max | cav_max "
        "| X_last | Y_last | <WX+WaX>_last_period |"
    )
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|")
    for c in cfgs:
        wsum = c.WX_last_period_mean + c.WaX_last_period_mean
        lines.append(
            f"| **{c.name}** "
            f"| {c.e_max:.4f} "
            f"| {c.h_min:.3e} "
            f"| {c.p_max:.3e} "
            f"| {c.cav_max:.3f} "
            f"| {c.X_last:+.4f} "
            f"| {c.Y_last:+.4f} "
            f"| {wsum:+.2e} |"
        )
    lines.append("")
    lines.append("## Max deviation vs baseline")
    lines.append("")
    lines.append(
        "| Configuration | d e_max | d h_min | d p_max | d X_last | d Y_last |"
    )
    lines.append("|---|---:|---:|---:|---:|---:|")
    for c in cfgs:
        if c is baseline:
            continue
        scale_X = max(abs(baseline.X_last), abs(baseline.Y_last), 1e-30)
        lines.append(
            f"| **{c.name}** "
            f"| {abs(c.e_max - baseline.e_max):.2e} "
            f"| {abs(c.h_min - baseline.h_min):.2e} "
            f"| {abs(c.p_max - baseline.p_max):.2e} "
            f"| {abs(c.X_last - baseline.X_last) / scale_X:.2e} "
            f"| {abs(c.Y_last - baseline.Y_last) / scale_X:.2e} |"
        )
    lines.append("")
    lines.append("## Recommended presets")
    lines.append("")
    lines.append(
        "```python"
    )
    lines.append(
        "from reynolds_solver import AusasAccelerationOptions"
    )
    lines.append("")
    lines.append(
        "# Pump harmonic / smooth periodic load:"
    )
    lines.append("accel_pump = AusasAccelerationOptions(")
    lines.append("    adaptive_dt=True,")
    lines.append("    dt_min=5e-4, dt_max=4e-3,")
    lines.append("    dt_grow=1.25, dt_shrink=0.5,")
    lines.append("    target_inner_low=80, target_inner_high=220,")
    lines.append("    dynamic_check_every=True,")
    lines.append("    check_every_min=5, check_every_max=25,")
    lines.append(")")
    lines.append("")
    lines.append(
        "# Diesel / sharp impulse load:"
    )
    lines.append("accel_diesel = AusasAccelerationOptions(")
    lines.append("    adaptive_dt=True,")
    lines.append("    dt_min=1e-4, dt_max=2e-3,   # tighter range")
    lines.append("    dt_grow=1.15, dt_shrink=0.4,")
    lines.append("    target_inner_low=60, target_inner_high=200,")
    lines.append("    dynamic_check_every=True,")
    lines.append("    check_every_min=5, check_every_max=15,")
    lines.append(")")
    lines.append("```")
    lines.append("")
    return "\n".join(lines)


def run_phase5_benchmark(
    N1: int = 100,
    N2: int = 12,
    dt: float = 2e-3,
    NT: int = 1500,
    tol_inner: float = 1e-6,
    max_inner: int = 5000,
    out_dir: str = "results",
    verbose: bool = True,
) -> dict:
    try:
        import cupy  # noqa: F401
    except Exception as exc:
        return {"skipped": f"cupy not available: {exc}"}

    from reynolds_solver import AusasAccelerationOptions

    settings = dict(
        N1=N1, N2=N2, dt=dt, NT=NT,
        tol_inner=tol_inner, max_inner=max_inner,
        scheme="rb",
    )

    cfgs = []

    # --- A : baseline (Phase 5.1B fused kernels, fixed dt) ---
    cfgs.append(_run_one(
        "A", "baseline (fixed dt)",
        accel=None,
        N1=N1, N2=N2, dt=dt, NT=NT,
        tol_inner=tol_inner, max_inner=max_inner, verbose=verbose,
    ))

    # --- B : adaptive dt only ---
    accel_B = AusasAccelerationOptions(
        adaptive_dt=True,
        dt_min=5e-4, dt_max=4e-3,
        dt_grow=1.25, dt_shrink=0.5,
        target_inner_low=80, target_inner_high=220,
        reject_if_not_converged=True,
    )
    cfgs.append(_run_one(
        "B", "adaptive dt only",
        accel=accel_B,
        N1=N1, N2=N2, dt=dt, NT=NT,
        tol_inner=tol_inner, max_inner=max_inner, verbose=verbose,
    ))

    # --- C : adaptive dt + dynamic check cadence ---
    accel_C = AusasAccelerationOptions(
        adaptive_dt=True,
        dt_min=5e-4, dt_max=4e-3,
        dt_grow=1.25, dt_shrink=0.5,
        target_inner_low=80, target_inner_high=220,
        reject_if_not_converged=True,
        dynamic_check_every=True,
        check_every_min=5, check_every_max=25,
    )
    cfgs.append(_run_one(
        "C", "adaptive + dynamic check",
        accel=accel_C,
        N1=N1, N2=N2, dt=dt, NT=NT,
        tol_inner=tol_inner, max_inner=max_inner, verbose=verbose,
    ))

    # --- D : pump-harmonic recommended preset ---
    # (Same as C with a slightly wider inner-window that lets dt grow
    # more aggressively on smooth phases.)
    accel_D = AusasAccelerationOptions(
        adaptive_dt=True,
        dt_min=5e-4, dt_max=5e-3,
        dt_grow=1.3, dt_shrink=0.5,
        target_inner_low=60, target_inner_high=260,
        reject_if_not_converged=True,
        dynamic_check_every=True,
        check_every_min=5, check_every_max=25,
    )
    cfgs.append(_run_one(
        "D", "pump-harmonic preset",
        accel=accel_D,
        N1=N1, N2=N2, dt=dt, NT=NT,
        tol_inner=tol_inner, max_inner=max_inner, verbose=verbose,
    ))

    # --- Report ---
    out_path = _ensure_results_dir(out_dir)
    md = _markdown_report(cfgs, settings)
    md_path = out_path / "phase5_acceleration_report.md"
    md_path.write_text(md, encoding="utf-8")

    json_path = out_path / "phase5_acceleration_report.json"
    json_path.write_text(
        json.dumps(
            {"settings": settings, "configs": [asdict(c) for c in cfgs]},
            indent=2,
        ),
        encoding="utf-8",
    )

    # --- Stdout summary ---
    print()
    print("=== Phase 5 acceleration benchmark ===")
    print(
        f"Grid {N1}x{N2}, dt={dt:.1e}, NT={NT}, tol={tol_inner:.0e}"
    )
    print()
    baseline = cfgs[0]
    for c in cfgs:
        sp = baseline.wall_time_s / max(c.wall_time_s, 1e-12)
        print(
            f"  {c.name} {c.description:<35s} "
            f"wall={c.wall_time_s:6.1f}s "
            f"speedup={sp:5.2f}x "
            f"accepted={c.accepted_steps:>4d} "
            f"mean_inner={c.mean_inner:5.0f} "
            f"e_max={c.e_max:.3f} "
            f"h_min={c.h_min:.3e}"
        )
    print()
    print(f"  report:  {md_path}")
    print(f"  json  :  {json_path}")

    return {
        "configs": [asdict(c) for c in cfgs],
        "settings": settings,
        "md_path": str(md_path),
        "json_path": str(json_path),
    }


def main(argv=None):
    argv = argv or sys.argv[1:]
    p = argparse.ArgumentParser(
        description="Phase 5 acceleration benchmark + markdown report."
    )
    p.add_argument("--N1", type=int, default=100)
    p.add_argument("--N2", type=int, default=12)
    p.add_argument("--dt", type=float, default=2e-3)
    p.add_argument("--NT", type=int, default=1500)
    p.add_argument("--tol-inner", type=float, default=1e-6)
    p.add_argument("--max-inner", type=int, default=5000)
    p.add_argument("--out-dir", type=str, default="results")
    p.add_argument("--quiet", action="store_true")
    args = p.parse_args(argv)

    res = run_phase5_benchmark(
        N1=args.N1, N2=args.N2, dt=args.dt, NT=args.NT,
        tol_inner=args.tol_inner, max_inner=args.max_inner,
        out_dir=args.out_dir,
        verbose=not args.quiet,
    )
    if "skipped" in res:
        print(f"  [SKIP] {res['skipped']}")
        return 0
    return 0


if __name__ == "__main__":
    sys.exit(main())
